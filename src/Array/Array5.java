public class Array5 {
    public static void main(String[] args) {
        int [][]a={
                {1,2,5,6,95,7},
                {1,3,4,56,7,89,0}
        };
        for (int i = 0; i <a.length; i++) {
            for (int j = 0; j <a[i].length ; j++) {
                System.out.println(a[i] [j]);

            }
        }

    }
}
